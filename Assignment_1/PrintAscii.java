public class PrintAscii{
    public static void main(String[]args){
        char ch1 = 'a';
        char ch2 = 'A';
        char ch3 = '%';

        System.out.print("The ASCII value of " + ch1 + " is: " + (int)ch1 + "\n");
        System.out.print("The ASCII value of " + ch2 + " is: " + (int)ch2 + "\n");
        System.out.print("The ASCII value of " + ch3 + " is: " + (int)ch3 + "\n");
    }
}